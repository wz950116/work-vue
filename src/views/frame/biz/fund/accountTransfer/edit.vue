        this.$refs.qmEdit.$refs.qmRouteEdit.editForm.inBankCode = value.data.bankCode
        this.$refs.qmEdit.$refs.qmRouteEdit.editForm.inOrganCode = value.data.organCode
      } else {
        this.$refs.qmEdit.$refs.qmRouteEdit.editForm.inBankCode = ''
